class IfelseDemo2 {
	public static void main(String[] args) {
		boolean x=true; 
		short y=50;
		long z=10;
		
		if(x==true) {
			System.out.println("the ans is true");		
		}
		else {
			System.out.println("the ans is false");
		}
	
		if(y==50) {
			System.out.println("short var is acceptable");
		}
		else {
			System.out.println("the short variable is not acceptable");
		}

		if (z==10) {
			System.out.println("the long var is acceptable");
		}
		else {
			System.out.println("long is not acceptable");
		}
	}
	
}

